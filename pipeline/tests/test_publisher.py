class TestContentJSONBuilder:
    @pytest.fixture
    def publisher(self):
        return Publisher(
            pipeline_session=MagicMock(),
            frontend_db_url="postgresql://test/db",
        )

    def test_extracts_visual_focus_from_prompt(self, publisher):
        version = MagicMock()
        version.generation_prompt = "Style prompt\n\nContent context: foo\n\nVisual focus: A Torah scroll on a wooden table"
        version.style_name = "watercolor"

        result = publisher._build_content_json(version, "conceptual_image")
        assert result["caption"] == "A Torah scroll on a wooden table"
        assert result["style"] == "watercolor"

    def test_falls_back_when_no_visual_focus(self, publisher):
        version = MagicMock()
        version.generation_prompt = "no focus line here"
        version.style_name = None

        result = publisher._build_content_json(version, "conceptual_image")
        assert result["caption"] == "Generated by Merkos Rambam pipeline"
        assert result["style"] == ""

    def test_handles_empty_prompt(self, publisher):
        version = MagicMock()
        version.generation_prompt = None
        version.style_name = "oil_painting"

        result = publisher._build_content_json(version, "conceptual_image")
        assert result["caption"] == "Generated by Merkos Rambam pipeline"
        assert result["style"] == "oil_painting"
