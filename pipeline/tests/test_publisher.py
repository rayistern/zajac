class TestFrontendURLBuilder:
    def test_uses_explicit_url_env_var(self, monkeypatch):
        monkeypatch.setenv("FRONTEND_DATABASE_URL", "postgresql://explicit/url")
        p = Publisher(pipeline_session=MagicMock())
        assert p.frontend_db_url == "postgresql://explicit/url"

    def test_builds_from_db_env_vars(self, monkeypatch):
        monkeypatch.delenv("FRONTEND_DATABASE_URL", raising=False)
        monkeypatch.setenv("DB_HOST", "dbhost")
        monkeypatch.setenv("DB_PORT", "5555")
        monkeypatch.setenv("DB_NAME", "mydb")
        monkeypatch.setenv("DB_USER", "myuser")
        monkeypatch.setenv("DB_PASS", "mypass")
        p = Publisher(pipeline_session=MagicMock())
        assert p.frontend_db_url == "postgresql://myuser:mypass@dbhost:5555/mydb"

    def test_explicit_constructor_arg_overrides_env(self, monkeypatch):
        monkeypatch.setenv("FRONTEND_DATABASE_URL", "postgresql://env/url")
        p = Publisher(
            pipeline_session=MagicMock(),
            frontend_db_url="postgresql://explicit/arg",
        )
        assert p.frontend_db_url == "postgresql://explicit/arg"


