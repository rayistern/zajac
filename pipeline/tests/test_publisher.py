"""Tests for the publisher module — pure logic (no live DB)."""

from __future__ import annotations

from unittest.mock import MagicMock

import pytest

from pipeline.publisher import (
    SUBTYPE_TO_CONTENT_TYPE,
    Publisher,
    _parse_sefaria_ref,
)


class TestSefariaRefParsing:
    def test_parses_full_ref_with_halacha(self):
        assert _parse_sefaria_ref("Mishneh Torah, Marriage 2:3") == ("Marriage", 2, 3)

    def test_parses_ref_without_halacha(self):
        assert _parse_sefaria_ref("Mishneh Torah, Marriage 2") == ("Marriage", 2, None)

    def test_parses_multiword_book(self):
        assert _parse_sefaria_ref("Mishneh Torah, The Chosen House 1:5") == (
            "The Chosen House",
            1,
            5,
        )

    def test_handles_missing_work_prefix(self):
        sefer, perek, halacha = _parse_sefaria_ref("Marriage 2:3")
        assert sefer == "Marriage"
        assert perek == 2
        assert halacha == 3

    def test_defaults_perek_to_1_when_missing(self):
        sefer, perek, halacha = _parse_sefaria_ref("SomeBook")
        assert perek == 1
        assert halacha is None


